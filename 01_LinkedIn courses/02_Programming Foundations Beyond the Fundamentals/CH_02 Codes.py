#LOOP
#Code that iterates,moving from beginning to end the process,then start over again
spices = [
    'salt',
    'pepper',
    'cumin',
    'turmeric',
]
#To print contents of the list
for spice in spices:
    print(spice)
print('No more boring omelettes!')

#WHILE LOOP
#Iterate to a custom endpoint
print('Counting to 100 by fives:')
i = 5
while i <= 100:
    print(i)
    i+=5
